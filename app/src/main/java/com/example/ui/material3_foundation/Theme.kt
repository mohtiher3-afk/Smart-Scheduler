@Composable
fun SmartSchedulerTheme(
    themeMode: String = "system", // "light", "dark", or "system"
    dynamicColorEnabled: Boolean = false,
    content: @Composable () -> Unit
) {
    val darkTheme = when (themeMode) {
        "dark" -> true
        "light" -> false
        else -> isSystemInDarkTheme()
    }

    val context = LocalContext.current
    val colorScheme = when {
        dynamicColorEnabled && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val customGradients = if (darkTheme) {
        CustomGradients(
            primary = androidx.compose.ui.graphics.Brush.horizontalGradient(listOf(DarkPrimary, DarkSecondary)),
            secondary = androidx.compose.ui.graphics.Brush.horizontalGradient(listOf(DarkSecondary, DarkTertiary)),
            cardBackground = androidx.compose.ui.graphics.Brush.verticalGradient(listOf(DarkSurface, DarkBackground))
        )
    } else {
        CustomGradients()
    }

    val semanticColors = if (darkTheme) {
        SemanticColors(
            success = DarkTertiary,
            completed = DarkTertiary,
            active = DarkTertiary
        )
    } else {
        SemanticColors()
    }

    CompositionLocalProvider(
        LocalDimens provides Dimens,
        LocalSpacing provides Spacing,
        LocalCustomGradients provides customGradients,
        LocalSemanticColors provides semanticColors
    ) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = AppTypography,
            shapes = AppShapes,
            content = content
        )
    }
}
